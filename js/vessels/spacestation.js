    spacestation.prototype.update = function (event) {

        if (this.lfHeld) {
            this.x += this.navspeed * (event.delta / 1000);
        }
        if (this.rtHeld) {
            this.x -= this.navspeed * (event.delta / 1000);
        }
        if (this.fwHeld) {
            this.y += this.navspeed * (event.delta / 1000);
        }
        if (this.bkHeld) {
            this.y -= this.navspeed * (event.delta / 1000);
        }

        if (this.show_interior == true) {
            this.interior.visible = true;
            this.char_aes.visible = true;
            this.char_sb.visible = true;
        } else {
            this.interior.visible = false;
            this.char_aes.visible = false;
            this.char_sb.visible = false;
        }
        for (var a = 0; a < this.controlledchars.length; a++) {
            this.controlledchars[a].update(event);
        }
        if (this.hostilechars == undefined) {
            this.hostilechars = [];
        }
        for (var b = 0; b < this.hostilechars.length; b++) {
            if (this.hostilechars[b].walk_queue == undefined) {
                this.hostilechars[b].walk_queue = [];
            }
            if (this.hostilechars[b].walk_queue.length == 0) {
                var gridBackup = this.grid.clone();
                var pathfinder = new PF.AStarFinder({
                    allowDiagonal: false,
                });
                var targetTileX;
                var targetTileY;
                var diff = 1000;
                for (var x = 0; x < this.controlledchars.length; x++) {
                    if (Math.abs(this.controlledchars[x].tileX - this.hostilechars[b].tileX) + Math.abs(this.controlledchars[x].tileY - this.hostilechars[b].tileY) < diff) {
                        targetTileX = this.controlledchars[x].tileX;
                        targetTileY = this.controlledchars[x].tileY;
                        diff = Math.abs(this.controlledchars[x].tileX - this.hostilechars[b].tileX) + Math.abs(this.controlledchars[x].tileY - this.hostilechars[b].tileY);
                    }
                }
                if (this.hostilechars[b].tileX != targetTileX || this.hostilechars[b].tileY != targetTileY) {
                    var path = pathfinder.findPath(this.hostilechars[b].tileX * 2 + 1, this.hostilechars[b].tileY * 2 + 1, targetTileX * 2 + 1, targetTileY * 2 + 1, gridBackup);
                    if (path.length > 0) {
                        for (var i = 0; i < path.length; i++) {
                            if (i != 0 && i % 2 == 0) {
                                for (var j = 0; j < path[i].length; j++) {
                                    path[i][j] = (path[i][j] - 1) / 2;
                                }
                                this.hostilechars[b].walk_queue.push(path[i]);
                            }
                        }
                    }
                }
            }
            this.hostilechars[b].update(event);
        }
    };
    

